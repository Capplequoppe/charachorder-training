// Group chords by word
export function getWordEntries(): WordEntry[] {
  const wordMap = new Map<string, WordEntry>();

  for (const entry of CHORD_DATA) {
    const existing = wordMap.get(entry.word);
    if (existing) {
      if (!existing.chords.includes(entry.chord)) {
        existing.chords.push(entry.chord);
      }
    } else {
      wordMap.set(entry.word, {
        word: entry.word,
        chords: [entry.chord],
        rank: entry.rank,
      });
    }
  }

  return Array.from(wordMap.values());
}

// Get words sorted by rank (lower = more important)
export function getWordsByRank(): WordEntry[] {
  return getWordEntries()
    .filter((w) => w.rank !== null)
    .sort((a, b) => (a.rank ?? Infinity) - (b.rank ?? Infinity));
}

// Search chords by chord pattern
export function searchByChord(query: string): ChordEntry[] {
  const normalizedQuery = query.toLowerCase();
  return CHORD_DATA.filter((entry) =>
    entry.chord.toLowerCase().includes(normalizedQuery)
  );
}

// Search chords by word
export function searchByWord(query: string): ChordEntry[] {
  const normalizedQuery = query.toLowerCase();
  return CHORD_DATA.filter((entry) =>
    entry.word.toLowerCase().includes(normalizedQuery)
  );
}

// Search by note combination (finger pattern)
export function searchByFingers(fingerNames: string[]): ChordEntry[] {
  const possibleChars = new Set<string>();
  for (const fingerName of fingerNames) {
    const finger = Object.values(Finger).find(f => f === fingerName) as Finger | undefined;
    if (finger) {
      const chars = getCharsForFinger(finger);
      chars.forEach((c) => possibleChars.add(c));
    }
  }

  return CHORD_DATA.filter((entry) => {
    const chordChars = new Set(entry.chord.toLowerCase().split(''));
    // Check if all chord chars are in possible chars
    for (const char of chordChars) {
      if (!possibleChars.has(char)) {
        return false;
      }
    }
    return true;
  });
}
